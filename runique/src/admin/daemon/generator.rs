/// Emits a complete `DynForm` impl block for a wrapper struct.
/// `save_param` is the parameter name ("db" or "_db"), `save_body` is the return expression.
fn write_dyn_form_wrapper(
    out: &mut String,
    comment: &str,
    wrapper: &str,
    form_path: &str,
    save_param: &str,
    save_body: &str,
) -> Result<(), String> {
    let _ = writeln!(out, "// ── {} ──", comment);
    let _ = writeln!(out, "struct {}(pub {});", wrapper, form_path);
    let _ = writeln!(out);
    let _ = writeln!(out, "#[async_trait]");
    let _ = writeln!(out, "impl DynForm for {} {{", wrapper);
    let _ = writeln!(out, "    async fn is_valid(&mut self) -> bool {{");
    let _ = writeln!(out, "        self.0.is_valid().await");
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);
    let _ = writeln!(
        out,
        "    async fn save(&mut self, {}: &DatabaseConnection) -> Result<(), DbErr> {{",
        save_param
    );
    let _ = writeln!(out, "        {}", save_body);
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);
    let _ = writeln!(out, "    fn get_form(&self) -> &Forms {{");
    let _ = writeln!(out, "        self.0.get_form()");
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);
    let _ = writeln!(out, "    fn get_form_mut(&mut self) -> &mut Forms {{");
    let _ = writeln!(out, "        self.0.get_form_mut()");
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
    Ok(())
}

/// Emits a `FormBuilder` closure assigned to `var_name`.
fn write_form_builder_closure(out: &mut String, var_name: &str, form_path: &str, wrapper: &str) {
    let _ = writeln!(
        out,
        "    let {}: FormBuilder = Arc::new(|_db: ADb, _vec: Vec<std::string::String>, data: StrMap, tera: ATera, csrf: String, method: Method| {{",
        var_name
    );
    let _ = writeln!(out, "        Box::pin(async move {{");
    let _ = writeln!(
        out,
        "            let form = {}::build_with_data(&data, tera, &csrf, method).await;",
        form_path
    );
    let _ = writeln!(
        out,
        "            Box::new({}(form)) as Box<dyn DynForm>",
        wrapper
    );
    let _ = writeln!(out, "        }})");
    let _ = writeln!(out, "    }});");
    let _ = writeln!(out);
}

/// Emits a `FormBuilder` closure with optional FK ChoiceField injection from the DB.
/// When `fk_cols` is empty, behaves identically to `write_form_builder_closure`.
fn write_form_builder_closure_fk(
    out: &mut String,
    var_name: &str,
    form_path: &str,
    wrapper: &str,
    fk_cols: &[&(
        String,
        String,
        Option<crate::admin::daemon::parser::FkDisplay>,
    )],
) {
    let db_param = if fk_cols.is_empty() { "_db" } else { "db" };
    let _ = writeln!(
        out,
        "    let {var}: FormBuilder = Arc::new(|{db}: ADb, _vec: Vec<std::string::String>, data: StrMap, tera: ATera, csrf: String, method: Method| {{",
        var = var_name,
        db = db_param,
    );
    let _ = writeln!(out, "        Box::pin(async move {{");
    let mut_kw = if fk_cols.is_empty() { "let" } else { "let mut" };
    let _ = writeln!(
        out,
        "            {mut_kw} form = {form}::build_with_data(&data, tera, &csrf, method).await;",
        mut_kw = mut_kw,
        form = form_path,
    );
    for (col, label, fk_opt) in fk_cols {
        let fk = fk_opt.as_ref().unwrap();
        let safe_var = col.replace('.', "_");
        let _ = writeln!(out, "            {{");
        let _ = writeln!(out, "                use sea_orm::ConnectionTrait;");
        let _ = writeln!(
            out,
            "                let _fk_opt_stmt_{safe} = sea_orm::sea_query::Query::select().expr(sea_orm::sea_query::Expr::cust(\"CAST(id AS TEXT)\")).expr(sea_orm::sea_query::Expr::cust(\"{fk_col}\")).from(sea_orm::sea_query::Alias::new(\"{fk_table}\")).to_owned();",
            safe = safe_var,
            fk_col = fk.col,
            fk_table = fk.table,
        );
        let _ = writeln!(
            out,
            "                let _fk_opt_choices_{safe}: Vec<(String, String)> = db.query_all(&_fk_opt_stmt_{safe}).await.unwrap_or_default().iter().filter_map(|row| {{ let id = row.try_get_by_index::<String>(0).ok()?; let lbl = row.try_get_by_index::<String>(1).ok()?; Some((id, lbl)) }}).collect();",
            safe = safe_var,
        );
        let _ = writeln!(
            out,
            "                form.get_form_mut().field_choices(\"{col}\", \"{label}\", _fk_opt_choices_{safe});",
            col = col,
            label = label,
            safe = safe_var,
        );
        let _ = writeln!(out, "            }}");
    }
    let _ = writeln!(
        out,
        "            Box::new({wrapper}(form)) as Box<dyn DynForm>",
        wrapper = wrapper,
    );
    let _ = writeln!(out, "        }})");
    let _ = writeln!(out, "    }});");
    let _ = writeln!(out);
}

/// Emits a `search_cond!` call for the search block.
/// FK columns are skipped — searching a raw FK ID makes no sense.
fn write_search_conditions(
    out: &mut String,
    list_display: &[(
        String,
        String,
        Option<crate::admin::daemon::parser::FkDisplay>,
    )],
    module: &str,
) {
    let searchable: Vec<&str> = list_display
        .iter()
        .filter(|(_, _, fk)| fk.is_none())
        .map(|(col, _, _)| col.as_str())
        .collect();

    if searchable.is_empty() {
        let _ = writeln!(
            out,
            "                let search_cond = search_cond!({module}::Entity => all_columns icontains search_str);",
            module = module
        );
    } else {
        let cols = searchable
            .iter()
            .map(|col| format!("\"{}\" icontains search_str", col))
            .collect::<Vec<_>>()
            .join(", ");
        let _ = writeln!(
            out,
            "                let search_cond = search_cond!({module}::Entity => or({cols}));",
            module = module,
            cols = cols
        );
    }
}

/// Builds a `DisplayConfig::new().columns_include(...).columns_exclude(...).list_filter(...)` chain.
fn build_display_chain(
    list_display: &[(
        String,
        String,
        Option<crate::admin::daemon::parser::FkDisplay>,
    )],
    list_exclude: &[String],
    list_filter: &[(String, String, u64)],
) -> String {
    let mut chain = "DisplayConfig::new()".to_string();
    if !list_display.is_empty() {
        let cols = list_display
            .iter()
            .map(|(c, l, _)| format!("(\"{}\", \"{}\")", c, l))
            .collect::<Vec<_>>()
            .join(", ");
        chain.push_str(&format!(".columns_include(vec![{}])", cols));
    }
    if !list_exclude.is_empty() {
        let cols = list_exclude
            .iter()
            .map(|c| format!("\"{}\"", c))
            .collect::<Vec<_>>()
            .join(", ");
        chain.push_str(&format!(".columns_exclude(vec![{}])", cols));
    }
    if !list_filter.is_empty() {
        let filters = list_filter
            .iter()
            .map(|(col, label, limit)| format!("(\"{}\", \"{}\", {}u64)", col, label, limit))
            .collect::<Vec<_>>()
            .join(", ");
        chain.push_str(&format!(".list_filter(vec![{}])", filters));
    }
    chain
}

/// Builds a comma-separated list of `GroupAction::*` expressions.
fn build_group_actions_str(group_action: &[(String, String, Option<String>)]) -> String {
    group_action
        .iter()
        .map(|(f, l, v)| match v {
            Some(val) => format!("GroupAction::val(\"{}\", \"{}\", \"{}\")", f, l, val),
            None => format!("GroupAction::bool(\"{}\", \"{}\")", f, l),
        })
        .collect::<Vec<_>>()
        .join(", ")
}

